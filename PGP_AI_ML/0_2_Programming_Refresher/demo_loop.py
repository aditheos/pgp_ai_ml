a = 0
print("While Loop \n")
while a < 10:
    print("The (WHILE) iteration# is " + str( a + 1 ))
    a = a + 1
    
print("\n")

print("For Loop \n")
a = 0

for a in range(0,10):
    print("The (FOR) iteration# is " + str( a + 1 ))
    
print("\n")